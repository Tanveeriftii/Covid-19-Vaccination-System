<tr>
  <td> <span>People with a history of severe allergic reaction to any component of the vaccine should not take it.

The vaccine has only been tested in children above 16 years of age. Therefore, at this time, WHO does not recommend vaccination of children below 16 years of age, even if they belong to a high-risk group</span> </td>
