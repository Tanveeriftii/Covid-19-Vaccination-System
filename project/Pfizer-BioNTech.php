      <td> <h4>Should pregnant women be vaccinated?</h4> </td>
    </tr>
    <tr>
      <td> <span>While pregnancy puts women at higher risk of severe COVID-19, very little data are available to assess vaccine safety in pregnancy.

Pregnant women may receive the vaccine if the benefit of vaccinating a pregnant woman outweighs the potential vaccine risks.

For this reason, pregnant women at high risk of exposure to SARS-CoV-2 (e.g. health workers) or who have comorbidities which add to their risk of severe disease, may be vaccinated in consultation with their health care provider.</span> </td>
</tr>
